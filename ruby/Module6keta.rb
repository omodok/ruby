module Module6keta
	module_function
	def hitoketa(cnt)
		puts "00000"+cnt.to_s
	end

	def hutaketa(cnt)
		puts "0000"+cnt.to_s
	end

	def sanketa(cnt)
		puts "000"+cnt.to_s
	end

	def yonketa(cnt)
		puts "00"+cnt.to_s
	end

	def goketa(cnt)
		puts "0"+cnt.to_s
	end

	def rokuketa(cnt)
		puts cnt
	end
end